    // Event listeners
    setupEventListeners() {
        // Tab değiştirme
        document.querySelectorAll('.tab-btn').forEach(btn => {
            btn.addEventListener('click', () => {
                const tabId = btn.dataset.tab;
                this.switchTab(tabId);
            });
        });
    }

    switchTab(tabId) {
        // Aktif tab'ı değiştir
        document.querySelectorAll('.tab-btn').forEach(btn => {
            btn.classList.remove('active');
        });
        document.querySelector(`[data-tab="${tabId}"]`).classList.add('active');

        // İçeriği değiştir
        document.querySelectorAll('.tab-content').forEach(content => {
            content.classList.remove('active');
        });
        document.getElementById(tabId).classList.add('active');
    }

    setupSearch() {
        const searchInput = document.getElementById('searchInput');
        searchInput.addEventListener('input', (e) => {
            const query = e.target.value.toLowerCase();
            this.filterStocks(query);
        });
    }

    filterStocks(query) {
        const stockItems = document.querySelectorAll('.stock-item');
        let firstVisible = null;
        
        stockItems.forEach(item => {
            const code = item.querySelector('.stock-code').textContent.toLowerCase();
            const name = item.querySelector('.stock-name').textContent.toLowerCase();
            
            if (code.includes(query) || name.includes(query)) {
                item.style.display = 'flex';
                if (!firstVisible) {
                    firstVisible = item;
                }
            } else {
                item.style.display = 'none';
            }
        });
        
        // İlk görünür hisseyi scroll ile göster
        if (firstVisible && query.length > 0) {
            setTimeout(() => {
                firstVisible.scrollIntoView({ 
                    behavior: 'smooth', 
                    block: 'center' 
                });
            }, 100);
        }
    }
