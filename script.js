    calculateRatios(data) {
        if (!data) return;

        const latest = data['2025/6'];
        if (!latest) return;

        // Basit oran hesaplamaları
        const currentRatio = latest.currentAssets / (latest.currentAssets * 0.6);
        const quickRatio = (latest.cash + latest.receivables) / (latest.currentAssets * 0.6);
        const equityRatio = latest.totalAssets * 0.4 / latest.totalAssets;
        const debtEquityRatio = latest.totalAssets * 0.6 / (latest.totalAssets * 0.4);

        document.getElementById('currentRatio').textContent = currentRatio.toFixed(2);
        document.getElementById('quickRatio').textContent = quickRatio.toFixed(2);
        document.getElementById('equityRatio').textContent = (equityRatio * 100).toFixed(1) + '%';
        document.getElementById('debtEquityRatio').textContent = debtEquityRatio.toFixed(2);
        
        // Karlılık oranları
        document.getElementById('roa').textContent = latest.roa.toFixed(1) + '%';
        document.getElementById('roe').textContent = latest.roe.toFixed(1) + '%';
    }

