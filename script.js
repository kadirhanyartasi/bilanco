    // Özet kartları güncelle
    updateSummaryCards(data) {
        if (!data) return;

        const latestPeriod = '2025/6';
        const previousPeriod = '2025/3';

        const updateCard = (elementId, value, previousValue) => {
            const element = document.getElementById(elementId);
            if (element) {
                element.textContent = this.formatNumber(value);
            }

            const changeElement = document.getElementById(elementId + 'Change');
            if (changeElement && previousValue) {
                const change = ((value - previousValue) / previousValue) * 100;
                changeElement.textContent = `${change > 0 ? '+' : ''}${change.toFixed(2)}%`;
                changeElement.className = `change ${change >= 0 ? 'positive' : 'negative'}`;
            }
        };

        updateCard('totalAssets', data[latestPeriod]?.totalAssets, data[previousPeriod]?.totalAssets);
        updateCard('currentAssets', data[latestPeriod]?.currentAssets, data[previousPeriod]?.currentAssets);
        updateCard('cash', data[latestPeriod]?.cash, data[previousPeriod]?.cash);
        updateCard('inventory', data[latestPeriod]?.inventory, data[previousPeriod]?.inventory);
    }

