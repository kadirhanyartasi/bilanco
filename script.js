    // Hisse listesini render et
    renderStockList() {
        const stockListElement = document.getElementById('stockList');
        stockListElement.innerHTML = '';

        this.stocks.forEach(stock => {
            const stockItem = document.createElement('div');
            stockItem.className = 'stock-item';
            stockItem.innerHTML = `
                <span class="stock-code">${stock.code}</span>
                <span class="stock-name">${stock.name}</span>
            `;
            
            stockItem.addEventListener('click', () => {
                this.selectStock(stock);
            });
            
            stockListElement.appendChild(stockItem);
        });
    }

    // Hisse seçimi
    async selectStock(stock) {
        // Aktif hisseyi güncelle
        document.querySelectorAll('.stock-item').forEach(item => {
            item.classList.remove('active');
        });
        event.target.closest('.stock-item').classList.add('active');

        // Seçilen hisseyi scroll ile görünür hale getir
        const selectedItem = event.target.closest('.stock-item');
        selectedItem.scrollIntoView({ 
            behavior: 'smooth', 
            block: 'center' 
        });

        this.currentStock = stock;
        
        // UI'yi güncelle
        document.getElementById('selectedStockName').textContent = stock.name;
        document.getElementById('selectedStockCode').textContent = stock.code;
        
        // Sayfa başlığını güncelle
        document.title = `${stock.code} - Bilanco Analiz Sistemi`;

        // Veriyi yükle
        await this.loadStockData(stock.file);
        
        // Otomatik olarak analiz sekmesine geç
        this.switchTab('analysis');
        
        // Tüm sayfaları güncelle
        this.updateAllPages();
    }

    // Hisse verilerini yükle - CSV yerine gömülü veri
    async loadStockData(filePath) {
        try {
            // Hisse kodunu dosya yolundan çıkar
            const stockCode = filePath.split('/').pop().replace('.csv', '');
            
            // Gömülü veri objesi
            const stockData = this.getEmbeddedStockData(stockCode);
            
            if (stockData) {
                this.currentData = stockData;
                console.log('Veri yüklendi:', this.currentData.length, 'satır');
            } else {
                console.error('Veri bulunamadı:', stockCode);
                this.currentData = null;
            }
        } catch (error) {
            console.error('Veri yüklenirken hata:', error);
            this.currentData = null;
        }
    }

